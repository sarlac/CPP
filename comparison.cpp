//program to do all comparison operations
#include <iostream>
using namespace std;

int main()
{
int x = 5;
int y = 3;
cout << " X>Y =" <<(x > y);    // returns 1 (true) because 5 is greater than 3
cout << "\n X==Y =" <<(x == y);
cout << "\n X!=Y =" <<(x != y);
cout << "\n X<Y =" <<(x < y);
cout << "\n X<=Y =" <<(x <= y);
cout << "\n X>=Y =" <<(x >= y);
return (0);
}
