//program to illustrate all the math operations

#include <iostream>
#include <cmath>
using namespace std;

int main()
{
cout << "\n Maximum number = " << max(5,10);                    // display maximum number from x,y
cout << "\n Minimum number = " << min(5,6);                     // display minimum number from x,y
cout << "\n Sqrt of 16 = " << sqrt(16);                         // display square root value of 16
cout << "\n Round value of 2.6 = " << round(2.6);               // display round up value of 2.6
cout << "\n log value of 2 = " <<log(2);                        // display log value of 2
cout << "\n nearest roundup value of 6.58 = " << floor(6.58);   //display nearest integer value
cout << "\n";
return (0);
}
