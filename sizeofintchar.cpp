//program to display size of char, float, int, double

#include <iostream>
using namespace std;

int main()
{

cout << "\n Size of Char is " << sizeof(char) << " byte" <<endl;
cout <<" \n Size of int is " << sizeof(int) << " bytes" << endl;
cout <<" \n Size of float is " << sizeof(float) << " bytes" <<endl;
cout <<" \n size of double is " << sizeof(double) << " bytes" <<endl;

return 0;
}
