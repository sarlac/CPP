 //Program for swapping two numbers

 #include <iostream>
 using namespace std;

 int main ()
 {
 int a=10;
 int b=20;
 cout << "\n value of a=" << a;
 cout << "\n Value of b= " <<b;
 int temp;
 temp=a;
 a=b;
 b=temp;
 cout << "\n\n Values of a and b after swapping\n";

 cout << "\n Value of a=" <<a;
 cout << "\n Value of b=" <<b;
 cout <<"\n";

 return 0;

 }
